<template>
  <el-dialog
    :title="title"
    :visible.sync="tmpVisible"
    :close-on-press-escape="false"
    :close-on-click-modal="false"
    :show-close="false"
    width="30%"
  >
    <div>
      <el-descriptions :column="2" border>
        <el-descriptions-item>
          <template slot="label">
            菜单名称
          </template>
          {{ form.name }}
        </el-descriptions-item>
        <el-descriptions-item>
          <template slot="label">
            菜单标识
          </template>
          {{ form.code }}
        </el-descriptions-item>
        <el-descriptions-item>
          <template slot="label">
            菜单图标
          </template>
          <e-icon :icon-name="form.icon ? form.icon : ''"/>
        </el-descriptions-item>
        <el-descriptions-item>
          <template slot="label">
            菜单类别
          </template>
          {{ form.type === 0 ? '目录' : (form.type === 1 ? '菜单' : '按钮')}}
        </el-descriptions-item>
        <el-descriptions-item>
          <template slot="label">
            路由地址
          </template>
          {{ form.url }}
        </el-descriptions-item>
        <el-descriptions-item>
          <template slot="label">
            组件路径
          </template>
          {{ form.path }}
        </el-descriptions-item>
        <el-descriptions-item>
          <template slot="label">
            是否外链
          </template>
          {{ form.link === 0 ? '否' : '是' }}
        </el-descriptions-item>
        <el-descriptions-item>
          <template slot="label">
            是否缓存
          </template>
          {{ form.cache === 0 ? '否' : '是' }}
        </el-descriptions-item>
        <el-descriptions-item>
          <template slot="label">
            是否可见
          </template>
          {{ form.visiable === 0 ? '否' : '是' }}
        </el-descriptions-item>
        <el-descriptions-item>
          <template slot="label">
            是否启用
          </template>
          {{ form.status === 0 ? '否' : '是' }}
        </el-descriptions-item>
        <el-descriptions-item>
          <template slot="label">
            创建时间
          </template>
          {{ form.createTime }}
        </el-descriptions-item>
        <el-descriptions-item>
          <template slot="label">
            描述
          </template>
          {{ form.description }}
        </el-descriptions-item>
      </el-descriptions>
    </div>
    <span slot="footer" class="dialog-footer">
      <el-button @click="tmpVisible = false">关 闭</el-button>
    </span>
  </el-dialog>
</template>

<script>
import api from '@/api/system/permission/jy-permission-menu'
export default {
  name: 'JyPermissionMenuDetail',
  props: {
    title: {
      type: String,
      default: 'Demo'
    },
    id: {
      type: String,
      default: null
    },
    visible: {
      type: Boolean,
      default: false
    }
  },
  data() {
    return {
      tmpVisible: this.visible,
      form: {
        id: '',
        name: '',
        code: '',
        description: ''
      }
    }
  },
  watch: {
    visible(newVal) {
      this.tmpVisible = newVal
      if (newVal) {
        this.getById(this.id)
      }
    },
    tmpVisible(newVal) {
      this.$emit('update:visible', newVal)
    },
    deep: true
  },
  methods: {
    getById(id) {
      api.getById(id).then(response => {
        this.form = response.data
      }).catch(e => {
        this.$notify.error({ title: '失败', message: '获取数据失败' })
      })
    }
  }
}
</script>

