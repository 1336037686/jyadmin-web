<script>
import logApi from '@/api/jy-log'
export default {
  name: 'JyLogDetail',
  props: {
    title: {
      type: String,
      default: 'Demo'
    },
    id: {
      type: String,
      default: null
    },
    visible: {
      type: Boolean,
      default: false
    }
  },
  data() {
    return {
      tmpVisible: this.visible,
      form: {
        id: '',
        handleName: '',
        handleDesc: '',
        ipAddress: '',
        ipArea: '',
        browser: '',
        application: '',
        requestPath: '',
        requestMethod: '',
        requestParam: '',
        requestTime: '',
        method: '',
        executeStatus: '',
        executeTime: '',
        responseData: '',
        errorDesc: '',
        username: ''
      }
    }
  },
  watch: {
    visible(newVal) {
      this.tmpVisible = newVal
      if (newVal) {
        this.getById(this.id)
      }
    },
    tmpVisible(newVal) {
      this.$emit('update:visible', newVal)
    },
    deep: true
  },
  methods: {
    getById(id) {
      logApi.getById(id).then(response => {
        this.form = response.data
      }).catch(e => {
        this.$notify.error({ title: '失败', message: '获取数据失败' })
      })
    }
  }
}
</script>

